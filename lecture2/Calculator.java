package lecture2;

public class Calculator {
    public void sum(int a, int b) {
        int result = a + b;
        System.out.println("Sum of a + b = " + result);
    }

    public void subtract(int a, int b) {
        int result = a - b;
        System.out.println("Sum of a - b = " + result);
    }

    public void multiply(int a, int b) {
        int result = a * b;
        System.out.println("Sum of a * b = " + result);
    }

    public void division(int a, int b) {
        int result = a / b;
        System.out.println("Sum of a / b = " + result);
    }
}

